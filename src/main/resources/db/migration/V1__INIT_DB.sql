CREATE TABLE topic
(
    id   BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name VARCHAR(255),
    CONSTRAINT pk_topic PRIMARY KEY (id)
);

CREATE TABLE origin
(
    id   BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name VARCHAR(255),
    CONSTRAINT pk_origin PRIMARY KEY (id)
);

CREATE TABLE news
(
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    description VARCHAR(255),
    topic_id    BIGINT,
    origin_id   BIGINT,
    CONSTRAINT pk_news PRIMARY KEY (id)
);

